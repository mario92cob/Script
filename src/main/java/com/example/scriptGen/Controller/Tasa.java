package com.example.scriptGen.Controller;

public class Tasa {
    private String plazoDD = null;
    private String plazoHH = null;
    private String tna = null;
    private String tem = null;
    private String ppp = null;
    private String codigoPro = null;

    public Tasa() {
    }

    public String getPlazoDD() {
        return this.plazoDD;
    }

    public void setPlazoDD(String plazoDD) {
        this.plazoDD = plazoDD;
    }

    public String getPlazoHH() {
        return this.plazoHH;
    }

    public void setPlazoHH(String plazoHH) {
        this.plazoHH = plazoHH;
    }

    public String getTna() {
        return this.tna;
    }

    public void setTna(String tna) {
        this.tna = tna;
    }

    public String getTem() {
        return this.tem;
    }

    public void setTem(String tem) {
        this.tem = tem;
    }

    public String getPpp() {
        return this.ppp;
    }

    public void setPpp(String ppp) {
        this.ppp = ppp;
    }

    public String getCodigoPro() {
        return this.codigoPro;
    }

    public void setCodigoPro(String codigoPro) {
        this.codigoPro = codigoPro;
    }
}
